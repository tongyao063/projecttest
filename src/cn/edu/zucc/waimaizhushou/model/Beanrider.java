package cn.edu.zucc.waimaizhushou.model;
import java.util.Date;

public class Beanrider {
	private int riderid;
	private String ridername;
	private Date rideronboarddate;
	private String rideridentity;
	private String riderpwd;
	
	public int getRiderid() {
		return riderid;
	}
	public void setRiderid(int riderid) {
		this.riderid = riderid;
	}
	public String getRidername() {
		return ridername;
	}
	public void setRidername(String ridername) {
		this.ridername = ridername;
	}
	public Date getRideronboarddate() {
		return rideronboarddate;
	}
	public void setRideronboarddate(Date rideronboarddate) {
		this.rideronboarddate = rideronboarddate;
	}
	public String getRideridentity() {
		return rideridentity;
	}
	public void setRideridentity(String rideridentity) {
		this.rideridentity = rideridentity;
	}
	public String getRiderpwd() {
		return riderpwd;
	}
	public void setRiderpwd(String riderpwd) {
		this.riderpwd = riderpwd;
	}
	
	
}
